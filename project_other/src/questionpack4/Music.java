package questionpack4;

public class Music {


	
	private String title;
	private String path;
	
	// 초기화
	public Music(String title, String path) {
		this.title = title;
		this.path = path;
	}
	
	// getter, setter 
	public String getTitle() {
		return title;
	}


	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}
	

	
	 
	
	
	


}
